#!/usr/bin/env node
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const catalog = JSON.parse(readFileSync(resolve(root, "seeds/catalog/starter-catalog.v1.json"), "utf8"));
const data = JSON.parse(readFileSync(resolve(root, "seeds/programs/starter-programs.v1.json"), "utf8"));
const out = resolve(root, "seeds/0008_starter_programs.sql");
const ts = "2026-07-19T00:00:00.000Z";
const workoutIds = new Set(catalog.workouts.map((w) => w.id));
const sql = (v) => (v == null ? "NULL" : `'${String(v).replaceAll("'", "''")}'`);
const lines = ["-- Generated by scripts/build-programs-seed-sql.mjs — do not edit by hand.", "DELETE FROM program_days;", "DELETE FROM programs;"];

for (const p of data.programs) {
  if (![7, 14, 30].includes(p.durationDays)) throw new Error(`${p.id}: bad durationDays`);
  if (!Array.isArray(p.days) || p.days.length !== p.durationDays) throw new Error(`${p.id}: days length must equal durationDays`);
  const nums = p.days.map((d) => d.day);
  if (new Set(nums).size !== nums.length) throw new Error(`${p.id}: duplicate day numbers`);
  for (let i = 1; i <= p.durationDays; i++) if (!nums.includes(i)) throw new Error(`${p.id}: missing day ${i}`);
  lines.push(`INSERT INTO programs (id, title, description, cover_object_key, duration_days, category, is_system, created_at) VALUES (${sql(p.id)}, ${sql(p.title)}, ${sql(p.description)}, ${sql(p.coverObjectKey ?? null)}, ${p.durationDays}, ${sql(p.category)}, ${p.isSystem ? 1 : 0}, ${sql(ts)});`);
  for (const d of p.days) {
    if (d.type !== "workout" && d.type !== "rest") throw new Error(`${p.id} d${d.day}: bad type`);
    if (d.type === "workout") {
      if (!d.workoutId || !workoutIds.has(d.workoutId)) throw new Error(`${p.id} d${d.day}: unknown workout ${d.workoutId}`);
    } else if (d.workoutId) throw new Error(`${p.id} d${d.day}: rest cannot have workoutId`);
    const id = `${p.id}-d${String(d.day).padStart(2, "0")}`;
    lines.push(`INSERT INTO program_days (id, program_id, day_number, type, workout_id, title, description) VALUES (${sql(id)}, ${sql(p.id)}, ${d.day}, ${sql(d.type)}, ${sql(d.type === "workout" ? d.workoutId : null)}, ${sql(d.title)}, ${sql(d.description ?? "")});`);
  }
}
writeFileSync(out, `${lines.join("\n")}\n`);
console.log(`Wrote ${out}: ${data.programs.length} programs, ${data.programs.reduce((n, p) => n + p.days.length, 0)} days`);
